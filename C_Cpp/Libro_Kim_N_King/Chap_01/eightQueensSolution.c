#include <stdio.h>

int v, i, j, k, l, s, a[99];

int main()
{
	for(
		scanf("%d",&s);
		*a - s;
		v = a[j *= v] - a[i], k = i < s, j += (
			v = j < s && (
				!k&&!! printf(
					2 + "\n\n%c" - (!l << !j),
					" #Q"[1^v?(1^j)&1:2]
					) &&
				++l||a[i] < s &&
				v &&
				v - i + j
				)
			) &&
			!(l%=s),
			v || (
				i == j ? a[i+=k] = 0 : ++a[i]
				)
			>= s*k &&
			++a[--i]
		);
}